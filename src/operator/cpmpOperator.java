package operator;

public class cpmpOperator {

    public static void main(String[] args) {
        System.out.println((3 == 4) + " ");
        System.out.println((3 != 4) + " ");
        System.out.println((3 > 4) + " ");
        System.out.println((4 > 3) + " ");

        System.out.println((3 == 3 && 4 == 7) + " "); //  히나만 거짓이면  false
        System.out.println((3 == 3 || 4 == 7) + " "); // 하나만 침이면 true

    } // end of main

}// end of class
